package restaurante.logica.base;

/**
 *
 * @author Estudiantes
 */
public abstract class Base  {
    
    public String conPollo;
    public String atollado;
    public String chino;
    public String res;
    public String pollo;
    public String pescado;

    public String getConPollo() {
        return conPollo;
    }

    public void setConPollo(String conPollo) {
        this.conPollo = conPollo;
    }

    public String getAtollado() {
        return atollado;
    }

    public void setAtollado(String atollado) {
        this.atollado = atollado;
    }

    public String getChino() {
        return chino;
    }

    public void setChino(String chino) {
        this.chino = chino;
    }

    public String getRes() {
        return res;
    }

    public void setRes(String res) {
        this.res = res;
    }

    public String getPollo() {
        return pollo;
    }

    public void setPollo(String pollo) {
        this.pollo = pollo;
    }

    public String getPescado() {
        return pescado;
    }

    public void setPescado(String pescado) {
        this.pescado = pescado;
    }
    
    
}
