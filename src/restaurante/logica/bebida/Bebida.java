package restaurante.logica.bebida;

/**
 *
 * @author Estudiantes
 */
public abstract class Bebida  {

    public String mora;
    public String mango;
    public String lulo;
    public String cocacola;
    public String pepsi;
    public String sprite; 

    public String getMora() {
        return mora;
    }

    public void setMora(String mora) {
        this.mora = mora;
    }

    public String getMango() {
        return mango;
    }

    public void setMango(String mango) {
        this.mango = mango;
    }

    public String getLulo() {
        return lulo;
    }

    public void setLulo(String lulo) {
        this.lulo = lulo;
    }

    public String getCocacola() {
        return cocacola;
    }

    public void setCocacola(String cocacola) {
        this.cocacola = cocacola;
    }

    public String getPepsi() {
        return pepsi;
    }

    public void setPepsi(String pepsi) {
        this.pepsi = pepsi;
    }

    public String getSprite() {
        return sprite;
    }

    public void setSprite(String sprite) {
        this.sprite = sprite;
    }
    


    
    

}
